/// Writes the initial community post catalogue to Firestore once.
/// Checks for an existing sentinel document before writing so it never
/// overwrites admin edits on subsequent launches.
class CommunitySeeder {
  static FirebaseFirestore get _db => FirebaseFirestore.instanceFor(
        app: Firebase.app(),
        databaseId: 'users',
      );

  static const _sentinelDoc = '_seed_v3';
  static Future<void> seedIfNeeded() async {
    try {
      final ref = _db.collection('community_posts').doc(_sentinelDoc);
      final snap = await ref.get();
      if (snap.exists) return;
      final existing = await _db.collection('community_posts').get();
      for (final doc in existing.docs) {
        await doc.reference.delete();
      }

      final batch = _db.batch();
      for (final post in _posts) {
        final docRef = _db.collection('community_posts').doc(post['id'] as String);
        batch.set(docRef, post);
      }

      // Sentinel — marks this seed version as done.
      batch.set(ref, {'seededAt': FieldValue.serverTimestamp()});

      await batch.commit();
      debugPrint('CommunitySeeder: seeded ${_posts.length} posts');
    } catch (e) {
      debugPrint('CommunitySeeder: skipped ($e)');
    }
  }

